export function getStateCodesForPincode(pincode: string): string[] {
  const prefix = pincode.trim().slice(0, 2);
  if (prefix.length < 2) return [];

  return PINCODE_PREFIX_TO_STATE_CODES[prefix] ?? [];
}

export function isStateCodeValidForPincode(
  stateCode: string,
  pincode: string,
): boolean {
  if (!stateCode || !pincode) {
    return true;
  }

  const allowedStateCodes = getStateCodesForPincode(pincode);
  if (allowedStateCodes.length === 0) {
    return false;
  }

  return allowedStateCodes.includes(stateCode);
}
